public class person {


    private String name;
    private String favColor;
    private  int age ;


    public void setName (String name ){
        this.name=name;

    }
    public String getName (){
        return name;
    }

    public void setAge(int age ){
        this.age=age;
    }
    public int  getAge (){
        return age;
    }


    public void setFavColor(String favColor){
        this.favColor=favColor;

    }

    public String getFavColor(){
        return favColor;
    }


}
